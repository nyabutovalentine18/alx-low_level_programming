int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
*add - adds two elemets
*@a: first element
*@b: second element
*Return: a + b
*/
int add(int a, int b)
{
return (a + b);
}

/**
*sub - subtracts two elements
*@a: first element
*@b: second element
*Return: a - b
*/
int sub(int a, int b)
{
return (a - b);
}

/**
*mul - multiplies two elemets
*@a: first element
*@b: second element
*Return: a * b
*/
int mul(int a, int b)
{
return (a * b);
}

/**
*div_y - divides two elements
*@a: first element
*@b: second element
*Return: a / b
*/
int div(int a, int b)
{
return (a / b);
}

/**
*mod - finds module of two numbers
*@a: first element
*@b: second element
*Return: a % b
*/
int mod(int a, int b)
{
return (a % b);
}
